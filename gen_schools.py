def get_dist(coords):
    # Упрощенный расчет расстояния
    if not coords: return 0
    dx = (coords[0] - ORIGIN[0]) * 111
    dy = (coords[1] - ORIGIN[1]) * 64 # Поправка на широту для Москвы
